namespace WindowExtras.Wpf.Menu;

/// <summary>
/// Defines the kind of the <see cref="MenuItem"/>.
/// </summary>
public enum MenuItemKind
{
    /// <summary>
    /// Displays the menu item using a text string.
    /// </summary>
    Text,

    /// <summary>
    /// Specifies that the menu item is a separator.
    /// </summary>
    Separator,
}
